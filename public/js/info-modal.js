/**
 * InfoModal — Shared "?" help modal with built-in chat.
 *
 * Shows a static explanation, then lets users ask follow-up questions
 * via the /api/help/chat endpoint.
 */
const InfoModal = (() => {
  let factorDefs = [];
  let currentKey = null;
  let chatHistory = [];
  let isLoading = false;

  // Static help entries
  const STATIC = {
    'composite-factors': {
      title: 'Composite Factors',
      body: `<p>Composite factors are high-level dimensions that combine scores from multiple analysis layers into a single interpretable number. They are analogous to the principal component scores (PC1\u2013PC5) in the original Coh-Metrix, extended here to eight factors (F1\u2013F8).</p>
<p>Each factor captures a distinct aspect of text quality \u2014 narrativity, syntactic accessibility, cohesion depth, argumentation strength \u2014 by weighting the relevant layer scores.</p>
<p>The radar chart shows all eight factors at once, making it easy to see the essay\u2019s profile at a glance. The dashed circle represents a reference average (70).</p>
<p><strong>Click the ? next to any individual factor</strong> for its definition and scoring formula.</p>`,
    },
    'overall-score': {
      title: 'Overall Cohesion Score',
      body: `<p>A single 0\u2013100 number summarizing the essay\u2019s overall text quality. It is a weighted average of all eight composite factors (F1\u2013F8).</p>
<p><strong>Default weights:</strong></p>
<ul>
<li>F6 Argumentation Quality: 20%</li>
<li>F4 Referential Cohesion: 15%</li>
<li>F5 Deep Cohesion: 15%</li>
<li>F1 Narrativity: 10%</li>
<li>F2 Syntactic Simplicity: 10%</li>
<li>F3 Word Concreteness: 10%</li>
<li>F7 Epistemic Calibration: 10%</li>
<li>F8 Engagement & Affect: 10%</li>
</ul>
<p>Argumentation is weighted most heavily because it is the strongest predictor of academic essay grades in the research literature (Wingate, 2012).</p>
<p>Weights can be customized via environment variables.</p>`,
    },
    'reader-profile': {
      title: 'Reader Profile (L11)',
      body: `<p>Layer 11 re-scores all metrics relative to a specific learner\u2019s profile. Text difficulty is inherently relational \u2014 what\u2019s complex for a beginner may be routine for an expert.</p>
<p><strong>Key metrics:</strong></p>
<ul>
<li><strong>Vocab level</strong> \u2014 CEFR level (A1\u2013C2) of the reader</li>
<li><strong>Syntax fluency</strong> \u2014 how comfortable the reader is with complex grammar (0\u20131)</li>
<li><strong>Domain expertise</strong> \u2014 beginner, intermediate, or advanced in the subject</li>
<li><strong>ZPD proximity</strong> \u2014 how close the text is to the reader\u2019s Zone of Proximal Development (Vygotsky, 1978). Values near 1.0 mean optimally challenging.</li>
<li><strong>Difficulty z-score</strong> \u2014 standard deviations above/below the reader\u2019s baseline</li>
<li><strong>Scaffold type</strong> \u2014 what kind of instructional support would help</li>
</ul>
<p>This is the most significant innovation of Neo-CohMetrix \u2014 no existing text analysis tool provides learner-relative difficulty scoring.</p>
<p>Requires a Learner ID to be set before analysis.</p>`,
    },
    'priority-feedback': {
      title: 'Priority Feedback',
      body: `<p>Three targeted, actionable suggestions generated by an AI tutor that has access to all layer scores and metric values.</p>
<p>The feedback focuses on:</p>
<ol>
<li><strong>Highest-need area</strong> \u2014 the dimension where improvement would have the biggest impact</li>
<li><strong>Argumentation</strong> \u2014 strength or gap in the essay\u2019s logical structure</li>
<li><strong>A strength to maintain</strong> \u2014 positive reinforcement for what works well</li>
</ol>
<p>Feedback deliberately avoids mentioning metric names or numbers \u2014 it translates the analysis into plain language a student can act on.</p>
<p>For deeper exploration, use the Help Chat (? button on any layer) or the Analysis Summary.</p>`,
    },
  };

  function open(key) {
    currentKey = key;
    chatHistory = [];
    clearChat();

    if (STATIC[key]) {
      show(STATIC[key].title, STATIC[key].body, key);
      return;
    }

    if (key.startsWith('factor:')) {
      const fId = key.replace('factor:', '');
      const f = factorDefs.find(d => d.id === fId);
      if (f) {
        const layerTags = f.layers.map(l => `<code>${l}</code>`).join(', ');
        const body = `<p>${f.description}</p>` +
          `<p><strong>Scoring formula:</strong> ${f.scoring}</p>` +
          `<p><strong>Source layers:</strong> ${layerTags}</p>`;
        show(f.id + ' ' + f.label, body, key);
        // Seed chat history with the explanation
        chatHistory.push({ role: 'assistant', text: f.description + ' Scoring: ' + f.scoring });
      }
      return;
    }
  }

  function show(title, bodyHtml, key) {
    document.getElementById('info-title').textContent = title;
    document.getElementById('info-body').innerHTML = bodyHtml;
    // Seed chat with the body text for context
    if (!chatHistory.length) {
      const div = document.createElement('div');
      div.innerHTML = bodyHtml;
      chatHistory.push({ role: 'assistant', text: div.textContent.substring(0, 800) });
    }
    document.getElementById('info-overlay').classList.add('open');
    document.getElementById('info-chat-input').focus();
  }

  function close() {
    document.getElementById('info-overlay').classList.remove('open');
    currentKey = null;
    chatHistory = [];
    isLoading = false;
  }

  function clearChat() {
    const msgs = document.getElementById('info-chat-messages');
    if (msgs) msgs.innerHTML = '';
  }

  function addChatMsg(role, text, loading) {
    const msgs = document.getElementById('info-chat-messages');
    const msg = document.createElement('div');
    msg.className = `info-chat-msg ${role}${loading ? ' loading' : ''}`;
    msg.textContent = text;
    msgs.appendChild(msg);
    msgs.scrollTop = msgs.scrollHeight;
    return msg;
  }

  async function sendChat() {
    if (isLoading) return;
    const input = document.getElementById('info-chat-input');
    const question = input.value.trim();
    if (!question) return;

    input.value = '';
    addChatMsg('user', question);
    chatHistory.push({ role: 'user', text: question });

    const loadingEl = addChatMsg('assistant', 'Thinking\u2026', true);
    isLoading = true;
    document.getElementById('info-chat-send').disabled = true;

    try {
      // Map the key to a help ID the backend understands
      const helpId = currentKey.startsWith('factor:') ? currentKey.replace('factor:', '') : currentKey;

      const resp = await Auth.apiFetch('/api/help/chat', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          id: helpId,
          question,
          history: chatHistory.slice(-6),
        }),
      });

      if (!resp.ok) throw new Error('Failed');
      const data = await resp.json();

      loadingEl.textContent = data.answer;
      loadingEl.classList.remove('loading');
      chatHistory.push({ role: 'assistant', text: data.answer });

      if (typeof TokenFooter !== 'undefined') TokenFooter.onApiResponse(data);
    } catch {
      loadingEl.textContent = 'Sorry, I couldn\u2019t generate a response. Please try again.';
      loadingEl.classList.remove('loading');
    }

    isLoading = false;
    document.getElementById('info-chat-send').disabled = false;
    input.focus();
  }

  function init() {
    document.getElementById('info-close-btn').addEventListener('click', close);
    document.getElementById('info-backdrop').addEventListener('click', close);
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && document.getElementById('info-overlay').classList.contains('open')) {
        close();
        e.stopPropagation();
      }
    });

    // Delegate clicks on .info-btn
    document.addEventListener('click', (e) => {
      const btn = e.target.closest('.info-btn');
      if (btn) {
        e.stopPropagation();
        const key = btn.getAttribute('data-info');
        if (key) open(key);
      }
    });

    // Chat send
    document.getElementById('info-chat-send').addEventListener('click', sendChat);
    document.getElementById('info-chat-input').addEventListener('keydown', (e) => {
      if (e.key === 'Enter' && !e.shiftKey) {
        e.preventDefault();
        sendChat();
      }
    });

    // Load factor definitions
    fetch('/api/meta').then(r => r.json()).then(meta => {
      if (meta.compositeFactors) factorDefs = meta.compositeFactors;
    }).catch(() => {});
  }

  return { init, open, close };
})();

document.addEventListener('DOMContentLoaded', () => InfoModal.init());
